// gen-town-data.mjs — export the real SubTracker ledger into Matte Hollow.
// Reads the live `subscriptions` table and writes ../mflux-town-kit/town-data.js
// as `window.TOWN_SUBS = [...]`, mapping each service to one of the six building
// types. matte-hollow.html loads that file (falling back to its sample town).
//
//   node scripts/gen-town-data.mjs            (run from the SubTracker root)
//
// Uses the same SUPABASE creds as the app (service role, server-only) from .env.

import { createClient } from "@supabase/supabase-js";
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));   // SubTracker/scripts
const root = join(here, "..");                           // SubTracker

// --- load .env (no dotenv dependency) ---
const env = {};
const envPath = join(root, ".env");
if (existsSync(envPath)) {
  for (const line of readFileSync(envPath, "utf8").split("\n")) {
    const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/);
    if (m) env[m[1]] = m[2].trim();
  }
}
const url = env.SUPABASE_URL || process.env.SUPABASE_URL;
const key = env.SUPABASE_SERVICE_ROLE_KEY || process.env.SUPABASE_SERVICE_ROLE_KEY;
if (!url || !key) {
  console.error("Missing SUPABASE_URL / SUPABASE_SERVICE_ROLE_KEY (.env or environment).");
  process.exit(1);
}

// --- service -> building type (the six sprite archetypes) ---
function typeFor(name, domain) {
  const t = (String(name) + " " + String(domain || "")).toLowerCase();
  const has = (...ks) => ks.some((k) => t.includes(k));
  if (has("verizon", "at&t", "att.com", "t-mobile", "tmobile", "comcast", "xfinity", "spectrum",
          "internet", "wireless", "mobile", "vpn", "nordvpn", "icloud", "google one", "dropbox",
          "backblaze", "storage", "fiber", "phone", "mint mobile", "visible")) return "telecom";
  if (has("instacart", "doordash", "uber eats", "ubereats", "grubhub", "freshpass", "vons",
          "albertsons", "hellofresh", "grocery", "gopuff", "walmart", "amazon fresh",
          "blue apron", "misfits", "thrive market", "shipt")) return "market";
  if (has("bank", "insurance", "credit", "robinhood", "coinbase", "mint.com", "ynab",
          "quickbooks", "turbotax", "experian", "credit karma", "finance", "wealthfront",
          "betterment", "rocket money", "empower")) return "bank";
  if (has("notion", "slack", "chatgpt", "openai", "claude", "anthropic", "github", "figma",
          "adobe", "microsoft", "office 365", "workspace", "zoom", "linear", "asana", "replit",
          "vercel", "aws", "jira", "grammarly", "canva", "1password", "todoist", "superhuman")) return "office";
  if (has("netflix", "disney", "hulu", "spotify", "apple music", "hbo", "max", "youtube",
          "paramount", "peacock", "prime video", "audible", "tidal", "crunchyroll",
          "game pass", "playstation", "nintendo", "twitch", "patreon")) return "home";
  return "cottage"; // the catch-all village cottage
}

const db = createClient(url, key, { auth: { persistSession: false } });

const { data, error } = await db
  .from("subscriptions")
  .select("service_name, service_domain, amount_cents, previous_amount_cents, status, next_renewal, evidence_count")
  .in("status", ["active", "past_due", "ending"])
  .order("amount_cents", { ascending: false, nullsFirst: false });

if (error) { console.error("query failed:", error.message); process.exit(1); }

const cents = (c) => Math.round(c) / 100;
const subs = (data || [])
  .filter((r) => r.amount_cents != null && r.amount_cents > 0)  // need a price to size a building
  .map((r) => ({
    name: r.service_name,
    type: typeFor(r.service_name, r.service_domain),
    cost: cents(r.amount_cents),
    status: r.status,
    ...(r.previous_amount_cents != null ? { prevCost: cents(r.previous_amount_cents) } : {}),
    ...(r.next_renewal ? { renewal: r.next_renewal } : {}),
    ...(r.evidence_count ? { receipts: r.evidence_count } : {}),
  }));

const outPath = join(root, "..", "mflux-town-kit", "town-data.js");
writeFileSync(outPath,
  "// Generated by SubTracker/scripts/gen-town-data.mjs — your live ledger as a town.\n" +
  "window.TOWN_SUBS = " + JSON.stringify(subs, null, 2) + ";\n");

const skipped = (data || []).length - subs.length;
console.log(`Wrote ${subs.length} buildings → ${outPath}`);
if (skipped > 0) console.log(`(skipped ${skipped} priceless sub${skipped === 1 ? "" : "s"} — no amount to size a building)`);
const monthly = subs.filter((s) => s.status !== "ending").reduce((t, s) => t + s.cost, 0);
console.log(`Town upkeep: $${monthly.toFixed(2)}/mo across ${subs.length} buildings.`);
